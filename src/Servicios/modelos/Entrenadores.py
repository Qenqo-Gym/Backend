class Entrenadores():
    
    def __init__(self, entrenador_id, nombre, celular, 
                 id_horario, cod_turno, des_turno, 
                 especializacion, fecha_incorporado, email, contraseña) -> None:
        self.entrenador_id = entrenador_id
        self.nombre = nombre
        self.celular = celular
        self.id_horario = id_horario
        self.cod_turno = cod_turno
        self.des_turno = des_turno
        self.especializacion = especializacion
        self.fecha_incorporado = fecha_incorporado
        self.email = email
        self.contraseña = contraseña